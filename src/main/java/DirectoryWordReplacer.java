	public static void replaceWordsInFile(File file, Map<String, String> wordMap) {
		try {
			Path filePath = Paths.get(file.getAbsolutePath());
			List<String> lines = Files.readAllLines(filePath, StandardCharsets.UTF_8);

			for (int i = 0; i < lines.size(); i++) {
				String line = lines.get(i);

				for (Map.Entry<String, String> entry : wordMap.entrySet()) {
					line = line.replaceAll("wp\\." + entry.getValue(), "wp." + entry.getKey());
				}

				lines.set(i, line);
			}

			Files.write(filePath, lines, StandardCharsets.UTF_8);

			System.out.println("Replacement in file " + file.getName() + " completed.");
		} catch (IOException e) {
			e.printStackTrace();
			System.out.println("Failed to replace words in file " + file.getName());
		}
	}

	public static void replaceWordsInFiles(String directoryPath, Map<String, String> wordMap) {
		File directory = new File(directoryPath);

		if (!directory.isDirectory()) {
			System.out.println("Provided path is not a directory.");
			return;
		}

		File[] files = directory.listFiles();

		if (files != null) {
			for (File file : files) {
				if (file.isFile()) {
					replaceWordsInFile(file, wordMap);
				}
			}
		}
	}
